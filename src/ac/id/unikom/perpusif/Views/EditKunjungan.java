    private void updateComboBox() {
                try {
                    AnggotaController.getInstance().getAll().forEach(anggota -> {
                        cbxnimPengunjung.addItem(anggota.getNim());
                    });
                }catch (Exception e) {
                    e.printStackTrace();
            }
    }
