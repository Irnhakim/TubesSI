    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // konfirmasi simpan
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Apakah anda yakin ingin mengubah data ini?","Konfirmasi",dialogButton);
        
        if(dialogResult == JOptionPane.YES_OPTION){
            // simpan data
            String nimAnggota = cbxnimPengunjung.getSelectedItem().toString();
            String tanggalKunjungan = txtTanggalKunjungan.getText();     
            String nimPetugas = SessionHelper.getInstance().getPetugas().getNim();
            
            try{
                KunjunganController.getInstance().update(kunjunganDipilih.getId().toString(), nimAnggota, nimPetugas , tanggalKunjungan);
                JOptionPane.showMessageDialog(this, "Data berhasil diubah");
                dispose();
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void cbxnimPengunjungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxnimPengunjungActionPerformed
        try {
            String nama = AnggotaController.getInstance().getNama(cbxnimPengunjung.getSelectedItem().toString());
            txtNamaPengunjung.setText(nama);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_cbxnimPengunjungActionPerformed

